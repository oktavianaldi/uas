<h2>Sejarah</h2>
            <div class="stori"> SMP Negeri 34 Semarang, Propinsi Jawa Tengah didirikan pada tanggal 18 Juli tahun 1992, berlokasi di Jl. Tlogomulyo Pedurungan, Kota Semarang. Pada mulanya sekolah ini sempat bergabung dengan SMP Negeri 15 Semarang. Dalam kurun waktu satu tahun kemudian, SMP Negeri 34 kemudian berpindah ke daerah kelurahan Tlogomulyo, Kecamatan Pedurungan Kota Semarang. Pada saat itu lingkungan dan wilayah sekitaran sekolah berada di tengah persawahan sehingga akses menuju ke sekolah masih sangat susah.
                    Tahun demi tahun SMP Negeri 34 Semarang selalu mengalami perkembangan dan kemajuan baik dari segi kualitas dan kuantitas. Seiring dengan pemekaran wilayah di kelurahan Tlogomulyo kecamatan Pedurungan, lingkungan sekitar sekolah yang semula persawahan terus berkembang menjadi pemukiman dan perumahan warga sehingga akses dan lingkungan menjadi semakin ramai.
                    Sejal awal didirikan hingga saat ini SMP Negeri 34 Semarang telah mengalami beberapa kali pergantian kepemimpinan, yakni
                    
                    
                    1. Soerip Suryani, menjabat pada periode 1992-2000
                    
                    2. Suharto, S.Pd. , menjabat pada periode 2000-2003
                    
                    3. Arif Basuku, S.Pd., menjabat pada periode 2003-2005
                    ?
                    
                    4. CH. Retnaningsih, menjabat pada periode 2005-2009 
                    ?
                    
                    5. Yuli Heriyani, S.Pd., menjabat pada periode 2009-2014
                    
                    6. Sutadi, S.Pd. M.Pd., menjabat pada periode 2014-sekarang
                    
                    Sesuai dengan peraturan yang ada bahwa menyusun perncanaan/program sekolah untuk jangka waktu yang datang merupakan suatu keharusan yang tidak bisa ditawar-tawar lagi, untuk hal tersebut sekolah mencoba menyusun Rencana Kerja Jangka Menengah (RKJM) salah satunya diimplementasikan dalam Program/Rencana Kerja Kepala Sekolah (RKKS) untuk jangka satu tahun ke depan, dengan harapan kegiatan-kegiatan rutin sekolah dan kegiatan-kegiatan pengembangan sekolah dapat lebih terprogram dan jelas arah tujuannya.
                    Kini SMP Negeri 34 Semarang terus bersinergi dengan berbagai pihak dalam upaya pengembangan sekolah.</div>
