<h2>VISI & MISI</h2>
            <div class="stori">VISI
                “Unggul dalam Prestasi Berlandaskan Iman dan Taqwa yang Berwawasan Lingkungan”
               <p>MISI</p> 
    <p>Melaksanakan pembelajaran dan bimbingan secara efektif untuk mengoptimalkan potensi yang dimiliki peserta didik.</p>
   <p>Menumbuhkembangkan penghayatan dan pengamalan terhadap agama yang dianut serta membentuk budi pekerti yang luhur.</p> 
<p>Menciptakan suasana yang kondusif untuk keefektifan seluruh kegiatan di lingkungan sekolah.</p>
<p>Melestarikan dan mengembangkan seni dan budaya bangsa.</p>
<p>Mengutamakan kerjasama dalam menyelesaikan tugas kependidikan.</p>
<p>Mengembangkan budaya kompetitif bagi peserta didik dalam upaya peningkatan prestasi di segala bidang.</p>
<p>Mewujudkan kegiatan sekolah berbasis partisipatif.</p>
<p>Mewujudkan kebersihan, keindahan lingkungan.</p>
<p>Mewujidkan perbaikan kerusakan lingkungan hidup.</p>
<p>Mewujudkan sekolah peduli pelestarian fungsi lingkungan.</p>
<p>Mewujudkan sekolah sejuk, nyaman dan sehat untuk belajar.</p></div>
